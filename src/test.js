function creatArr(str, l) {     
  var len = str.length
  let newStr = ''//截取之后返回的新字符串

  if (len > l) {
    newStr = str.substr(0, l)
    arr.push(newStr)
    str = str.replace(newStr, '')
    creatArr(str, l)
  } else {
    arr.push(str)
  }
  return arr;
}

